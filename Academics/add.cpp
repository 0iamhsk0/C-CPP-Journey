#include <iostream>
using namespace std;
int main()
{
  float a, b;
  cout << "Enter two number: ";
  cin >> a >> b;
  cout << "Your number are " << a << " " << b << endl;
  cout << a << "+" << b << "=" << a + b << endl;
  cout << a << "-" << b << "=" << a - b << endl;
  cout << a << "*" << b << "=" << a * b << endl;
  cout << a << "/" << b << "=" << a / b << endl;
}